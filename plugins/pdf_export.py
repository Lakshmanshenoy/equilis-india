"""
plugins/pdf_export.py
PDF export — renders CompanySnapshot analysis to a branded PDF research report.

Primary API: PDFReportExporter
  .export_analysis(snapshot, ..., dest_dir=None) -> str (path)
  .export_peer_comparison(peer_result, ..., dest_dir=None) -> str (path)
  .export_scenario(scenario_result, ..., dest_dir=None) -> str (path)

Requires: fpdf2>=2.7.9 (pip install fpdf2)
"""

